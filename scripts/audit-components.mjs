#!/usr/bin/env node
/**
 * audit-components.mjs
 *
 * Phase 0: scan all docs/**\/*.mdx files and produce a frequency table of
 * every JSX component used. Run this before writing transformers to know
 * what you're dealing with.
 *
 * Usage:
 *   node scripts/audit-components.mjs [--root /path/to/docusaurus/root]
 *
 * Output:
 *   - Console table: component name → count, strategy, coverage
 *   - Writes docs/llms-txt/COMPONENT_REGISTRY.md (a human-readable registry)
 */

import { readFileSync, writeFileSync, existsSync, readdirSync, statSync } from "fs";
import { join, relative, dirname } from "path";
import { fileURLToPath } from "url";
import { COMPONENT_REGISTRY, scanMarkdownImports } from "./mdx-to-md.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));

const args = process.argv.slice(2);
const rootArgIdx = args.indexOf("--root");
const PROJECT_ROOT = rootArgIdx !== -1 ? args[rootArgIdx + 1] : process.cwd();
const DOCS_DIR = join(PROJECT_ROOT, "docs");

function findFiles(dir, ext, results = []) {
  if (!existsSync(dir)) return results;
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) findFiles(full, ext, results);
    else if (entry.endsWith(ext)) results.push(full);
  }
  return results;
}

const mdxFiles = findFiles(DOCS_DIR, ".mdx");
console.log(`\n🔍 Auditing ${mdxFiles.length} MDX files in ${DOCS_DIR}\n`);

// Track: componentName → { count, files: Set, exampleLine }
const components = new Map();

// Components imported from a Markdown file (rendered via transclusion) — these
// are handled by the transformer even though they aren't in the static registry.
const transcludedNames = new Set();

// Regex to find JSX component usage (PascalCase open/self-closing tags)
const JSX_TAG_RE = /<([A-Z][A-Za-z0-9]*)[\s>\/]/g;

for (const filePath of mdxFiles) {
  const content = readFileSync(filePath, "utf8");
  const relPath = relative(PROJECT_ROOT, filePath);

  // Record markdown-imported (transcluded) component names for this file.
  for (const name of Object.keys(scanMarkdownImports(content))) transcludedNames.add(name);

  // Skip content inside code fences AND inline code spans. Inline code is a
  // major source of false positives: type generics (`WorkflowContext<Self>`),
  // CLI placeholders (`-w <ID>`), etc. are not JSX components.
  const stripped = content
    .replace(/```[\s\S]*?```/g, "")
    .replace(/~~~[\s\S]*?~~~/g, "")
    .replace(/`[^`\n]*`/g, "");

  let match;
  JSX_TAG_RE.lastIndex = 0;
  const lineMap = new Map();
  const lines = stripped.split("\n");
  // Build char-offset to line mapping for example lines
  let offset = 0;
  for (let i = 0; i < lines.length; i++) {
    lineMap.set(offset, i + 1);
    offset += lines[i].length + 1;
  }

  const seen = new Set(); // dedupe per-file
  for (const line of lines) {
    let m;
    JSX_TAG_RE.lastIndex = 0;
    while ((m = JSX_TAG_RE.exec(line)) !== null) {
      const name = m[1];
      seen.add(name);
      if (!components.has(name)) {
        components.set(name, { count: 0, files: new Set(), exampleLine: line.trim() });
      }
      components.get(name).exampleLine = line.trim();
    }
  }
  for (const name of seen) {
    const entry = components.get(name);
    entry.count++;
    entry.files.add(relPath);
  }
}

// Sort by frequency descending
const sorted = [...components.entries()].sort((a, b) => b[1].count - a[1].count);

// Display
console.log(`${"Component".padEnd(30)} ${"Files".padStart(6)} ${"Strategy".padEnd(18)} ${"Coverage"}`);
console.log("─".repeat(75));

let covered = 0;
let uncovered = 0;

for (const [name, data] of sorted) {
  const strategy = COMPONENT_REGISTRY[name] || (transcludedNames.has(name) ? "transclude" : "❓ UNKNOWN");
  const isCovered = !!COMPONENT_REGISTRY[name] || transcludedNames.has(name);
  if (isCovered) covered++;
  else uncovered++;

  const coverageIcon = isCovered ? "✅" : "❌";
  console.log(
    `${name.padEnd(30)} ${String(data.count).padStart(6)} ${strategy.padEnd(18)} ${coverageIcon}`
  );
}

const total = covered + uncovered;
const pct = total > 0 ? Math.round((covered / total) * 100) : 0;

console.log(`\n📊 Coverage: ${covered}/${total} components (${pct}%)`);
if (uncovered > 0) {
  console.log(`   ❌ ${uncovered} unknown components — add them to COMPONENT_REGISTRY in scripts/mdx-to-md.mjs`);
}

// Write COMPONENT_REGISTRY.md to the repo root (NOT under docs/, which would
// publish this dev artifact as a live Docusaurus page).
const registryPath = join(PROJECT_ROOT, "COMPONENT_REGISTRY.md");
const lines = [
  "# Component Registry",
  "",
  `Generated by \`scripts/audit-components.mjs\`. Run to update.`,
  "",
  `**Coverage: ${covered}/${total} (${pct}%)**`,
  "",
  "| Component | File Count | Strategy | Covered |",
  "|-----------|------------|----------|---------|",
];

for (const [name, data] of sorted) {
  const strategy = COMPONENT_REGISTRY[name] || (transcludedNames.has(name) ? "transclude" : "UNKNOWN");
  const isCov = !!COMPONENT_REGISTRY[name] || transcludedNames.has(name);
  lines.push(`| \`${name}\` | ${data.count} | \`${strategy}\` | ${isCov ? "✅" : "❌"} |`);
}

lines.push("", "## Strategy Definitions", "");
lines.push("| Strategy | Description |");
lines.push("|----------|-------------|");
lines.push("| `tabs` | Parent container for language/option tabs. Accumulates TabItems. |");
lines.push("| `tabitem` | A single tab. Content is rendered with a **Label** header. |");
lines.push("| `transparent` | Wrapper with no content of its own. Tags stripped, children passed through. |");
lines.push("| `related-read` | Converts `readList` prop to a markdown link list. |");
lines.push("| `captioned-image` | Renders `src` + `caption` as `![caption](src)`. |");
lines.push("| `strip-tag` | JSX tags stripped, text content preserved. |");
lines.push("| `strip-block` | Entire component and all children removed. No content value. |");
lines.push("| `details` | `<summary>` becomes a heading; body expanded inline. |");

writeFileSync(registryPath, lines.join("\n") + "\n", "utf8");
console.log(`\n📄 Written: ${relative(PROJECT_ROOT, registryPath)}`);
